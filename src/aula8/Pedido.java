package aula8;

public class Pedido {
	String nomeComprador;
	String nomeProduto;
	float precoProduto;
	int Id;
	
	public Pedido(String nomeComprador, String nomeProduto, float precoProduto , int Id) {
		this.nomeComprador = nomeComprador;
		this.nomeProduto = nomeProduto;
		this.precoProduto = precoProduto;
		this.Id = Id;
	}
	
	
	
	
}
